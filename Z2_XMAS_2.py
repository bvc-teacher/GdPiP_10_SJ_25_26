import turtle
t = turtle.Turtle()
t.speed(0) 

s = turtle.Screen()
s.setup(width=800, height=600) 
t.pensize(2)
t.pencolor('black')

t.goto(0, 270) 
t.pendown()

t.fillcolor('yellow')
t.begin_fill()











t.end_fill()

t.hideturtle()
s.exitonclick()